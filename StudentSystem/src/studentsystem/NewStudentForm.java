    private void continueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continueBtnActionPerformed
        // TODO add your handling code here:
        Student student = new Student();
        try {
            int id = Integer.parseInt(idTextField.getText());
            if(isDuplicatedId(id)) {
                JOptionPane.showMessageDialog(null, "This id was used, please try another one!", "Duplicate id", JOptionPane.WARNING_MESSAGE);
                return;
            }
            student.setId(id);
            
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Student id must be a integer number", "Invalid id", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(nameTextField.getText().trim() == "") {
            JOptionPane.showMessageDialog(null, "Student name must not be blank", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            student.setName(nameTextField.getText());
        }
        
        if(maleRadioBtn.isSelected()) {
            student.setGender('M');
        } else {
            student.setGender('F');
        }
        
        try {
            double score = Double.parseDouble(scoreTextField.getText());
            if(score < 0 || score > 10) {
                throw new NumberFormatException();
            }
            
            student.setScore(score);
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Student score is not valid", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(addressTextField.getText().trim() == "") {
            JOptionPane.showMessageDialog(null, "Student address must not be blank", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            student.setAddress(addressTextField.getText());
        }
        
        student.setNote(noteTextField.getText());
        
        if(fileChooser.getSelectedFile() == null) {
            JOptionPane.showMessageDialog(null, "Student image must inclue", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            try {
                student.changeAndSaveImage(fileChooser.getSelectedFile());
                students.add(student);
                IOUtil.writeStudent(students);
            } catch(IOException e) {
                System.err.println(e);
            }
        }
       
        cb.accept(student);
        this.dispose();
        
        JOptionPane.showMessageDialog(this, "Student saved", "Information", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_continueBtnActionPerformed

