    public void scaleImage() {
        ImageIcon icon = new ImageIcon("images\\user.png");
        Image img = icon.getImage();
        Image imgScale = img.getScaledInstance(studentImg.getWidth(), studentImg.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);
        studentImg.setIcon(scaledIcon);
    }
    
    public void fillStudentImg(String path) {
        ImageIcon icon = new ImageIcon(path);
        Image img = icon.getImage();
        Image imgScale = img.getScaledInstance(studentImg.getWidth(), studentImg.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(imgScale);
        studentImg.setIcon(scaledIcon);
    }

